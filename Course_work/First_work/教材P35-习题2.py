import math

PI = math.pi
print("半径为2.11的圆球体积:" + "{:.2f}".format(2.11 ** 3 * PI * 4 / 3))
print("外圆斑晶为16.2，内圆半径为9.4的圆环面积：" + "{:.2f}".format((16.2 ** 2 - 9.4 ** 2) * PI))
print("底面半径为66，高为24，2的圆柱体的体积" + "{:.2f}\n".format(
    66 ** 2 * PI * 24.2) + "底面半径为66，高为24，2的圆柱体的表面积" + "{:.2f}".format(
    66 ** 2 * PI * 2 + 66 * 2 * PI * 24.2))
